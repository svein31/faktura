const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("lang") || "pl");
  const setLanguage = useCallback((l) => {
    setLang(l);
    localStorage.setItem("lang", l);
  }, []);
  const t = useCallback((key) => (translations[lang] && translations[lang][key]) || (translations.pl[key]) || key, [lang]);
  const value = useMemo(() => ({ lang, setLanguage, t }), [lang, setLanguage, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) return { lang: "pl", setLanguage: () => {}, t: (k) => k };
  return ctx;
}
